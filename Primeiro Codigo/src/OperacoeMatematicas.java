public class OperacoeMatematicas {
    public static void main(String[] args) {
        int n = 49;
        int m = 10;

        int soma = n + m;
        int subtracao = n - m;
        int multiplicaocao = n * m;
        double divisao = n / m;
        int resto = n % m;
        // incremento de n
        n++;
        int incremento = n;
        // decremento  de m
        m--;
        int decremento = m;

        System.out.println(soma);
        System.out.println(subtracao);
        System.out.println(multiplicaocao);
        System.out.println(divisao);
        System.out.println(resto);
        System.out.println(incremento);
        System.out.println(decremento);
    }
}

        /*
        + -> Adição
        1 + 2
        - -> Subtração
        19 - 5
        * -> Multiplicação
        5 * 7
        / -> Divisão
        6 / 3
        % -> Resto da Divisão
        7 % 3
        ++ -> Incremento
        n++
        -- -> Decremento
        n--
        */






